    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        if (this.provincias.getSelectedItem().toString().equals("Seleccione una provincia")
                || this.ciudades.getSelectedItem().toString().equals("Seleccione una ciudad")||this.nombre.getText().equals("")||this.apellido.getText().equals("")||this.direccion.getText().equals("")||this.telefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor selecione una provincia y una ciudad");
        } else {
            this.asignar();
        }
        this.limpiar();
    }//GEN-LAST:event_guardarActionPerformed

    private void asignar(){
        Datos ds = new DatosSalida();
        Persona d = new Persona();
        Territorio t = new Territorio();
        d.setNombre(this.nombre.getText());
        d.setApellido(this.apellido.getText());
        t.setDireccion(this.direccion.getText());
        d.setTelefono(this.telefono.getText());
        t.setProvincia(this.provincias.getSelectedItem().toString());
        t.setCiudad(this.ciudades.getSelectedItem().toString());
        ds.InsertarPersona(d, t);
    }

